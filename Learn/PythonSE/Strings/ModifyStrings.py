# Upper Case
a = "Mi precioso"
print(a.upper())

# Lower Case
a = "Feliz NAVIDAD"
print(a.lower())

# Remove Whitespace from the beginning
a = "   Ayuda, por favor!"
print(a.strip())

# Reemplazar
print(a.replace("Ayuda", "Auxilio").strip())

# Separar en lista
a = "Hello, World!"
print(a.split(","))